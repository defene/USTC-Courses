#include "i8259A.h"

void init8259A(void){
	//你需要填写这里
	outb(0x21, 0xFF); //shield the interrupt
	outb(0xA1, 0xFF);

	outb(0x20, 0x11); //initialize main chip
	outb(0x21, 0x20);
	outb(0x21, 0x04);
	outb(0x21, 0x3);

	outb(0xA0, 0x11); //initialize slave chip
	outb(0xA1, 0x28);
	outb(0xA1, 0x02);
	outb(0xA1, 0x01);

	return;
}
